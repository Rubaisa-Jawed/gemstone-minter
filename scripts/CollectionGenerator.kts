fun chooseColor(id: Int): String{
    return when(id){
        0 -> "#01b0bc"
        1 -> "#010ebc"
        2 -> "#8e54b5"
        3 -> "#00b300"
        4 -> "#ff0000"
        5 -> "#ffffff"
        else -> "#011e1e"
    }
}

